#include<iostream>
using namespace std;
const int MaxNum = 10001;

int main()
{
    int L, M;
    int left, right;
    while(cin >> L >> M)
    {
        if (L == 0 && M == 0)
            break;
        
        int TotalNum[MaxNum] = {0};
        for(int i = 0; i < M; i++)
        {
            cin >> left >> right;
            for(int j = left; j <= right; j++)
            {
                TotalNum[j] = 1;
            }
        }

        int num = 0;
        for(int k = 0; k < MaxNum; k++)
        {
            if(TotalNum[k])
                num++;
        }
        cout << L + 1 - num << endl;
    }
}